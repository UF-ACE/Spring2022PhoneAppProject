public class MainActivity extends AppCompatActivity {
    public static final String EXTRA_MESSAGE = "com.example.aceapp.MESSAGE";
    String appId = "testapp-mftvr";

    private Button button;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        button = (Button) findViewById(R.id.button);
        button.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                openConfirmation();
            }
        });



        //Initializes MongoDB realm on application
        Realm.init(this);
        App app = new App(new AppConfiguration.Builder(appId).build());


        //Logs a user in to the realm, tells users if successful or not
        Credentials credentials = Credentials.emailPassword("email", "pass");
        app.loginAsync(credentials, new App.Callback<User>() {
            @Override
            public void onResult(App.Result<User> result) {
                if (result.isSuccess()) {
                    Log.v("User", "Logged in anonymously");
                }
                else {
                    Log.v("User", "failed to log in");
                }
            }
        });
    }

    public void openConfirmation() {
        Intent intent = new Intent(this, ConfirmationActivity.class);
        startActivity(intent);
    }
}